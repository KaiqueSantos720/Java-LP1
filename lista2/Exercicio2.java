public class Exercicio2 {
	public static void main (String args[]) {
		
		Integer soma = 0;
		
		for(Integer i = 1; i <= 1000; i++) {
			soma = soma + i;
		}
		
		System.out.println("A soma de 1 até 1000 é: " + soma);
		
	}

}
